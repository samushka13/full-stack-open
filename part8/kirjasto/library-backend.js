const typeDefs = `
  type Book {
    title: String!
    author: String!
    published: Int!
    genres: [String!]!
  }

  type Author {
    name: String!
    born: Int
    bookCount: Int
  }

  type Query {
    bookCount: Int
    authorCount: Int
    allBooks(author: String, genre: String): [Book!]!
    allAuthors: [Author!]!
  }

  type Mutation {
    addBook(
      title: String!
      author: String!
      published: Int!
      genres: [String!]!
    ): Book
    editAuthor(
      name: String!
      setBornTo: Int!
    ): Author
  }
`;

const resolvers = {
  Query: {
    bookCount: () => books.length,
    authorCount: () => authors.length,
    allBooks: (_, args) => {
      let result = books.map(({ id, ...rest }) => rest);

      if (args.author) {
        result = result.filter((b) => b.author === args.author);
      }

      if (args.genre) {
        result = result.filter((b) => b.genres.includes(args.genre));
      }

      return result;
    },
    allAuthors: () =>
      authors.map((a) => ({
        name: a.name,
        born: a.born || null,
        bookCount: books.filter((b) => b.author === a.name).length,
      })),
  },
  Mutation: {
    addBook: (_, args) => {
      const book = { ...args, id: randomUUID() };
      books = books.concat(book);

      if (!authors.some((a) => a.name === args.author)) {
        authors = authors.concat({ id: randomUUID(), name: args.author });
      }

      return { title: book.title, author: book.author };
    },
    editAuthor: (_, args) => {
      const match = authors.find((a) => a.name === args.name);

      if (!match) {
        return null;
      }

      authors = authors.map((a) =>
        a.id === match.id ? { ...match, born: args.setBornTo } : a
      );

      return { name: match.name, born: args.setBornTo };
    },
  },
};

const server = new ApolloServer({
  typeDefs,
  resolvers,
});

startStandaloneServer(server, {
  listen: { port: 4000 },
}).then(({ url }) => {
  console.log(`Server ready at ${url}`);
});
